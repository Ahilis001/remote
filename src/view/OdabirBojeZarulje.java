    private void jButtonOdabirBojeZaruljeOdaberiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonOdabirBojeZaruljeOdaberiActionPerformed
        switch(buttonGroupOdabirBojaZaruljeBoje.getSelection().getActionCommand()){
            case "ZaruljaONWhiteWH":{
                Pocetna.jButtonRasvjetaZaruljaOdabirBoje.setBackground(new java.awt.Color(255, 255, 255));
                odabranaBoja = "ZaruljaONWhiteWH";
                break;
            }
        
            case "ZaruljaONCyanWH":{
                Pocetna.jButtonRasvjetaZaruljaOdabirBoje.setBackground(new java.awt.Color(0, 255, 255));
                odabranaBoja = "ZaruljaONCyanWH";
                break;
            }
            
            case "ZaruljaONMagentaWH":{
                Pocetna.jButtonRasvjetaZaruljaOdabirBoje.setBackground(new java.awt.Color(255, 0, 255));
                odabranaBoja = "ZaruljaONMagentaWH";
                break;
            }
            
            case "ZaruljaONYellowWH":{
                Pocetna.jButtonRasvjetaZaruljaOdabirBoje.setBackground(new java.awt.Color(255, 255, 0));
                odabranaBoja = "ZaruljaONYellowWH";
                break;
            }
            
            case "ZaruljaONRedWH":{
                Pocetna.jButtonRasvjetaZaruljaOdabirBoje.setBackground(new java.awt.Color(255, 0, 0));
                odabranaBoja = "ZaruljaONRedWH";
                break;
            }
            
            case "ZaruljaONGreenWH":{
                Pocetna.jButtonRasvjetaZaruljaOdabirBoje.setBackground(new java.awt.Color(0, 255, 0));
                odabranaBoja = "ZaruljaONGreenWH";
                break;
            }
            
            case "ZaruljaONBlueWH":{
                Pocetna.jButtonRasvjetaZaruljaOdabirBoje.setBackground(new java.awt.Color(0, 0, 255));
                odabranaBoja = "ZaruljaONBlueWH";
                break;
            }
            
            default:{
                odabranaBoja = "ZaruljaONWH";
                break;
            }
        
        }
        noviProzor.setVisible(false);
    }//GEN-LAST:event_jButtonOdabirBojeZaruljeOdaberiActionPerformed

